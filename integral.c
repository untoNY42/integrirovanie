/**
 * integral.c - Адаптивное вычисление определённого интеграла методом Симпсона
 * Интегрирует func(x) на [a, b] до достижения точности eps2.
 * Автоматически удваивает число разбиений n.
 */
#include <math.h>
#include <stdio.h>
#include <stdlib.h> /* Для abs, если потребуется, хотя здесь только fabs */

double integral(double (*func)(double), double a, double b,
                double eps2, int *iter, int verbose) {
    /* Объявление всех переменных в начале блока */
    int n = 2;            /* Начальное количество разбиений (четное) */
    int i = 0;            /* Счетчик итераций */
    double h = 0.0;       /* Шаг интегрирования */
    double S_prev = 0.0;  /* Предыдущее значение интеграла */
    double S_curr = 0.0;  /* Текущее значение интеграла */
    double sum = 0.0;     /* Накопительная сумма для формулы Симпсона */
    double diff = 0.0;    /* Разница между текущим и предыдущим значениями */
    
    /* Вычисляем первое приближение для n = 2 */
    h = (b - a) / n;
    /* Формула Симпсона для n=2: (h/3) * (f(a) + 4*f(a+h) + f(b)) */
    S_prev = (h / 3.0) * (func(a) + 4.0 * func(a + h) + func(b));

    if (verbose) {
        printf("[Интеграл] Итер %3d: n=%4d, S=%.10f\n", i + 1, n, S_prev);
    }

    /* Основной цикл уточнения интеграла */
    while (1) {
        n *= 2; /* Удваиваем количество разбиений */
        h = (b - a) / n;
        
        sum = func(a) + func(b); /* Крайние точки с коэффициентом 1 */

        /* Цикл суммирования внутренних точек */
        /* k=1, 3, 5... (нечетные) имеют коэффициент 4 */
        /* k=2, 4, 6... (четные) имеют коэффициент 2 */
        for (int k = 1; k < n; k++) {
            if (k % 2 == 1) {
                sum += 4.0 * func(a + k * h);
            } else {
                sum += 2.0 * func(a + k * h);
            }
        }
        
        S_curr = (h * sum) / 3.0;
        diff = fabs(S_curr - S_prev);

        if (verbose) {
            printf("[Интеграл] Итер %3d: n=%4d, S=%.10f, |Δ|=%.2e\n",
                   i + 2, n, S_curr, diff);
        }

        /* Проверка условия остановки */
        if (diff < eps2) {
            break;
        }

        /* Подготовка к следующей итерации */
        S_prev = S_curr;
        i++;

        /* Защита от бесконечного цикла на случай расходимости или слишком жесткой точности */
        if (i > 10000) {
            fprintf(stderr, "Warning: Integral did not converge after %d iterations.\n", i);
            break;
        }
    }

    *iter = i + 1; /* Общее количество итераций (включая первую) */
    return S_curr;
}